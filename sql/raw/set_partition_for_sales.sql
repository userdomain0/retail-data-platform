--CREATE PARTITION FUNCTION
--以年月做分區
CREATE PARTITION FUNCTION pf_sales_date (INT)
AS RANGE RIGHT FOR VALUES (
    20180101, 20180201, 20180301, 20180401, 20180501, 20180601
    , 20180701, 20180801, 20180901, 20181001, 20181101
    , 20181201
);


CREATE PARTITION SCHEME ps_sales_date
AS PARTITION pf_sales_date
ALL TO ([PRIMARY]);